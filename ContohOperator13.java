public class ContohOperator13 {
    public static void main(String[] args) {
        int a=60;
        int b=20;
        int c=10;


        System.err.println("Aritmatika Operator");
        System.err.println("Bilangan a adalah" + a);
        System.err.println("Bilangan b adalah" + b);
        System.err.println("Bilangan c adalah" + c);
        System.err.println("a + b + c =" + (a+b+c));
        System.err.println("a - b - c =" + (a-b-c));
        System.err.println("a * b =" + (a*b));
        System.err.println("a / b ="+(a/b));
        System.err.println("a % b =" + (a%b));
        System.err.println("a + b * c =" + (a+b*c));
        System.err.println("(a + b) * c ="+((a+b)*c));
    }
}
